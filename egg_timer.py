while True:
    choice = input("Bạn muốn luộc trứng kiểu nào? (1-lòng đào / 2-bán chín / 3-chín hẳn): ")
    if choice == '1':
        print("→ Luộc 6 phút nhé!")
        break
    elif choice == '2':
        print("→ Luộc 9 phút nhé!")
        break
    elif choice == '3':
        print("→ Luộc 12 phút nhé!")
        break
    else:
        print("Vui lòng nhập 1, 2 hoặc 3.")
